package ArrayObjeto;

public class Computadores {
    String marca;
    int cantidadMemoria;
    String caracteristicaProcesador;
    String sistemaOperativo;
    double precio;
    public Computadores(String marca, int cantidadMemoria, String caracteristicaProcesador, String sistemaOperativo,
            double precio) {
        this.marca = marca;
        this.cantidadMemoria = cantidadMemoria;
        this.caracteristicaProcesador = caracteristicaProcesador;
        this.sistemaOperativo = sistemaOperativo;
        this.precio = precio;
    }
    

}
